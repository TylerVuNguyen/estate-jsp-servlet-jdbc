package com.vujavaweb.dto;

public class BuildingType {
	// code of building type
	private String value;
	// building name
	private String name;

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
